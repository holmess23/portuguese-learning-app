function markStatus(status){
  if(!deck.length) return;
  const w = deck[deckPos];
  progress[w.id] = status;
  saveProgress();
  renderSidebar();
  if(currentBlock === 'review' && status === 'known'){
    // remove from review deck immediately
    deck.splice(deckPos,1);
    if(deckPos >= deck.length) deckPos = 0;
    renderCardsView();
    return;
  }
  renderMiniGrid();
  updateSubStat();
  nextCard();
}

